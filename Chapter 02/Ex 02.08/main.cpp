/*
 *  Put the codes before and after modifying together.
 */

#include<iostream>
using namespace std;

int main()
{
	cout << "Output before modifying:" << endl;
	cout << "\62\115\n" << endl;
	cout << "Output after modifying:" << endl;
	cout << "\62\t\115\n" << endl;
	cout << "------end------" << endl;
	return 0;
}