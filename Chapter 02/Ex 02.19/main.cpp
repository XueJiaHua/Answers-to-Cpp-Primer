/*
 *  The key differences between pointers and references:
 *  1) A pointer has an object, whereas a reference don't;
 *  2) A pointer can hold a NULL value, whereas a reference don't;
 *  3) A reference must be initialized on declaration, whereas a pointerdon't;
 *  4) References are used to refer an existing variable in another name whereas pointers are used to store address of variable.
 */